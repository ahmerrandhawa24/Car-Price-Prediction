def compare_cars(make1, model1, make2, model2):

    # Find cars
    car1 = df[(df['Make'] == make1) & (df['Model'] == model1)]
    car2 = df[(df['Make'] == make2) & (df['Model'] == model2)]

    if car1.empty or car2.empty:
        print("❌ One or both cars not found")
        return

    car1 = car1.iloc[0]
    car2 = car2.iloc[0]

    print("\n🚗 Car Comparison\n")

    # Price comparison
    print(f"{make1} {model1} Price: {car1['Price']}")
    print(f"{make2} {model2} Price: {car2['Price']}")

    diff = abs(car1['Price'] - car2['Price'])
    print(f"\n💰 Price Difference: {diff}")

    # Feature comparison
    print("\n⚙️ Feature Comparison:")
