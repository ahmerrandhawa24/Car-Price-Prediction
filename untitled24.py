def best_value_car(budget):

    print(f"\n💎 Finding best car under {budget}...\n")

    # Filter cars within budget
    filtered_df = df[df['Price'] <= budget].copy()

    if filtered_df.empty:
        print("❌ No cars found in this budget")
        return

    # Avoid division errors
    filtered_df = filtered_df[filtered_df['Car_Age'] > 0]

    # Create score
    filtered_df['Score'] = (
        (filtered_df['Engine HP'] + filtered_df['Mileage']) /
        (filtered_df['Price'] * filtered_df['Car_Age'])
    )

    # Get best car
    best_car = filtered_df.sort_values(by='Score', ascending=False).iloc[0]

    # Display result
    print("🏆 Best Value Car:\n")
    print(f"🚗 {best_car['Make']} {best_car['Model']}")
    print(f"💰 Price: {best_car['Price']}")
    print(f"⚡ Engine HP: {best_car['Engine HP']}")
    print(f"📅 Car Age: {best_car['Car_Age']}")
    print(f"🚗 Mileage: {best_car['Mileage']}")
    print(f"⭐ Score: {round(best_car['Score'], 6)}")

best_value_car(30000)

