def depreciation_predictor(make, model, years):

    print(f"\n📉 Predicting future price for {make} {model} after {years} years...\n")

    # Find the car
    car = df[(df['Make'] == make) & (df['Model'] == model)]

    if car.empty:
        print("❌ Car not found")
        return

    car = car.iloc[0]

    # Create copy
    future_car = car.copy()

    # Increase age
    future_car['Car_Age'] = future_car['Car_Age'] + years

    # Recalculate derived features
    future_car['HP_per_Cylinder'] = (
        future_car['Engine HP'] / future_car['Engine Cylinders']
        if future_car['Engine Cylinders'] != 0 else 0
    )

    future_car['Mileage'] = (
        (future_car['highway MPG'] + future_car['city mpg']) / 2
    )

    # Convert to DataFrame
    input_df = pd.DataFrame([future_car])

    # Match columns
    input_df = input_df.reindex(columns=X_train.columns, fill_value=0)

    # Predict future price
    future_price = rf_model.predict(input_df)[0]

    # Current price
    current_price = car['Price']

    print(f"💰 Current Price: {current_price}")
    print(f"📉 Predicted Price after {years} years: {round(future_price, 2)}")

    # Depreciation %
    depreciation = ((current_price - future_price) / current_price) * 100

    print(f"📊 Depreciation: {round(depreciation, 2)}%")

